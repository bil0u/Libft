#include <stdlib.h>

void	ft_tabstrdel(char ***tab)
{
	int		i;

	if (tab)
	{
		if (*tab)
		{
			i = 0;
			while ((*tab)[i])
			{
				free((*tab)[i]);
				i++;
			}
			free(*tab);
		}
		*tab = NULL;
	}
}
