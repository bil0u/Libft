#include <stdint.h>

intmax_t	ft_pgcd(intmax_t a, intmax_t b)
{
	intmax_t	tmp;

	while (b != 0)
	{
		tmp = a % b;
		a = b;
		b = tmp;
	}
	return (a);
}
